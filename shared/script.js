function captureUtm() {
  if (!refs.utmField) return;
  const keys = ["utm_source", "utm_medium", "utm_campaign", "utm_term", "utm_content"];
  const params = new URLSearchParams(window.location.search);
  const incoming = {};

  keys.forEach((key) => {
    const value = params.get(key);
    if (value) incoming[key] = value;
  });

  const storedRaw = localStorage.getItem("lp_utm") || "{}";
  const stored = JSON.parse(storedRaw);
  const merged = { ...stored, ...incoming };

  if (Object.keys(incoming).length) {
    localStorage.setItem("lp_utm", JSON.stringify(merged));
  }

  refs.utmField.value = JSON.stringify(merged);
  if (refs.utmNote && merged.utm_source) {
    refs.utmNote.textContent = `${t("utmHintPrefix")} utm_source=${merged.utm_source}`;
  } else if (refs.utmNote) {
    refs.utmNote.textContent = "";
  }
}

