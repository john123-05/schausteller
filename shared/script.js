function bindCalculator() {
  if (!refs.calcForm || !refs.calcResult) return;

  refs.calcForm.addEventListener("submit", (event) => {
    event.preventDefault();
    const visitors = Number(refs.calcForm.querySelector("#calc_visitors")?.value || 0);
    const price = Number(refs.calcForm.querySelector("#calc_price")?.value || 0);
    const conversion = Number(refs.calcForm.querySelector("#calc_conversion")?.value || 0);

    const sales = Math.max(0, visitors) * (Math.max(0, conversion) / 100);
    const revenue = sales * Math.max(0, price);
    const locale = state.lang === "de" ? "de-DE" : "en-US";
    const salesFormatted = new Intl.NumberFormat(locale).format(Math.round(sales));
    const revenueFormatted = new Intl.NumberFormat(locale, { style: "currency", currency: "EUR" }).format(revenue);

    if (state.lang === "de") {
      refs.calcResult.textContent = `${t("calcResultLabel")}: ${salesFormatted} verkaufte Bilder • ${revenueFormatted} Umsatz`;
    } else {
      refs.calcResult.textContent = `${t("calcResultLabel")}: ${salesFormatted} sold images • ${revenueFormatted} revenue`;
    }
  });
}

